package com.raju.abstracts;

public class MyTest {
	private String name;
	private String place;
	
	public MyTest(String name, String place) {
		super();
		this.name = name;
		this.place = place;
	}

	public String getName() {
		return name;
	}

	public String getPlace() {
		return place;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setPlace(String place) {
		this.place = place;
	}
	
	
}
