package com.raju.elseifcondition;

public class ElseIfCondition {

	public static void main(String[] args) {
		int a = 80;
		if(a>90) {
			System.out.println("A+");
		}else if(a>80) {
			System.out.println("A");			
		}else if(a>70) {
			System.out.println("B");			
		}else if(a>60) {
			System.out.println("C");			
		}else if(a>45) {
			System.out.println("D");			
		}else {
			System.out.println("Not");
		}

	}

}
