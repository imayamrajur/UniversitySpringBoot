package com.raju.datatypefloat;

public class DataTypeFloat {

	public static void main(String[] args) {
		float a = 200.40f;
		float b = 150.50f;
		System.out.println("Add Float Value = "+(a+b));
		System.out.println("Multiple Float Value = "+(a*b));
		System.out.println("Sub Float Value = "+(a-b));

	}

}
