package com.raju.datatypedouble;

public class DataTypeDouble {

	public static void main(String args[]) {
		double a = 200.200d;
		double b = 100.50d;
		System.out.println("Add Double Value ="+(a+b));
		System.out.println("Multiple Double Value ="+(a*b));
		System.out.println("Sub Double Value ="+(a-b));
		
		
	}

}
